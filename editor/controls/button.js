/**
 * An enumeration of mouse buttons.
 *
 * @class Button
 * @static
 */

export const Button = {

	/**
	 * The main mouse button, usually the left one.
	 *
	 * @property MAIN
	 * @type Number
	 * @static
	 * @final
	 */

	MAIN: 0,

	/**
	 * The auxiliary mouse button, usually the middle one.
	 *
	 * @property AUXILIARY
	 * @type Number
	 * @static
	 * @final
	 */

	AUXILIARY: 1,

	/**
	 * The secondary mouse button, usually the right one.
	 *
	 * @property SECONDARY
	 * @type Number
	 * @static
	 * @final
	 */

	SECONDARY: 2

};
